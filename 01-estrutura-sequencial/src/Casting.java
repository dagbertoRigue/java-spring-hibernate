
public class Casting {

	public static void main(String[] args) {
		int x;
		double y;
		x = 5;
		y = 2 * x;
		System.out.printf("x é do tipo int : %d %n", x);
		System.out.printf("y é do tipo double : %f %n", y);
		System.out.println("A variável y faz um cast implícito da variável x");
		
		System.out.println();
		System.out.println("------------------------------------------------------------------------------------------");
	}

}
